def match_name(target, source_cats):
    """Prefix-match category names, returning shortest candidate."""
    t = target.strip().lower()
    for c in source_cats:
        if c.strip().lower() == t:
            return c
    candidates = [c for c in source_cats if c.strip().lower().startswith(t)]
    return min(candidates, key=len) if candidates else None


_MONTH_ABBR = {m.lower(): i for i, m in enumerate(calendar.month_abbr) if m}

def _label_to_date(label):
    """'Apr-26' → date(2026, 4, 1). Day is always 1."""
    mon_str, yr_str = label.split("-")
    mon = _MONTH_ABBR[mon_str.lower()]
    yr  = int(yr_str)
    yr  = yr + 2000 if yr < 100 else yr
    return date(yr, mon, 1)


def _col_to_label(col_idx, ws_raw):
    """Return the Mon-YY header label for a 1-indexed column."""
    v = ws_raw.cell(5, col_idx).value
    return v if v else f"col{col_idx}"


def _last_date_col(ws):
    """Find rightmost column in row 5 that has a non-None value."""
    for c in range(ws.max_column, 3, -1):
        if ws.cell(5, c).value is not None:
            return c
    return 3


def _build_cat_to_row(ws, min_row=6, cat_col=3):
    """Map category name → first row number in ws (raw data rows only — first occurrence wins)."""
    result = {}
    for r in range(min_row, ws.max_row + 1):
        cat = ws.cell(r, cat_col).value
        if cat is not None:
            key = str(cat).strip()
            if key not in result:
                result[key] = r
    return result


# Formula blocks in US_CPI sheet of FINAL.xlsx (extended each new month via Translator)
FORMULA_BLOCKS = [
    (392,  774),   # block 1 (intermediate)
    (778,  1160),  # MoM (%) — source for Tabela
    (1163, 1545),  # MM3MA   — source for Tabela
    (1549, 1931),  # MM6MA   — source for Tabela
    (1935, 2317),  # YoY     — source for Tabela
]

# Row ranges to READ from FINAL.xlsx US_CPI (data_only=True) for each variation type
VARIATION_READ_BLOCKS = {
    "MoM":   (392,  774),
    "MM3MA": (1163, 1545),
    "MM6MA": (1549, 1931),
    "YoY":   (1935, 2317),
}


def _last_formula_col(ws):
    """Return the rightmost column that has content in the first row of any formula block. 0 = none found."""
    max_col = 0
    for first_row, _ in FORMULA_BLOCKS:
        for c in range(ws.max_column, 3, -1):
            if ws.cell(first_row, c).value is not None:
                if c > max_col:
                    max_col = c
                break
    return max_col


def read_variation_block(ws_do, row_start, row_end, max_col):
    """
    Read cached formula values from a US_CPI variation block (data_only workbook).
    Returns {category: {col_idx: float}} — only columns with numeric cached values.
    """
    result = {}
    for row in ws_do.iter_rows(min_row=row_start, max_row=row_end,
                                min_col=3, max_col=max_col, values_only=True):
        cat = row[0]
        if cat is None:
            continue
        row_data = {}
        for i, v in enumerate(row[1:], start=4):
            if isinstance(v, (int, float)):
                row_data[i] = v
        if row_data:
            result[str(cat).strip()] = row_data
    return result


def _merge_variations(python_var, excel_var):
    """
    Merge Excel formula block values with Python-computed values.
    Excel values take priority (authoritative for cached months).
    Python fills columns not yet cached in Excel (the new month just added).
    """
    merged = {}
    for block in ["MoM", "MM3MA", "MM6MA", "YoY"]:
        m = {}
        py_b = python_var.get(block, {})
        ex_b = excel_var.get(block, {})
        for cat in set(py_b) | set(ex_b):
            combined = dict(py_b.get(cat, {}))
            combined.update(ex_b.get(cat, {}))  # Excel overrides where cached
            m[cat] = combined
        merged[block] = m
    return merged


def _excel_recalculate(xls_path):
    """
    Open xls_path in Excel (COM), force recalculation of all formulas, save, close.
    Restores the formula cache that openpyxl clears on every save.
    Safe to call when other Excel files are already open (only quits if we launched Excel).
    """
    try:
        import win32com.client
        try:
            excel = win32com.client.GetActiveObject("Excel.Application")
            launched = False
        except Exception:
            excel = win32com.client.Dispatch("Excel.Application")
            launched = True

        excel.Visible = False
        excel.DisplayAlerts = False
        wb = excel.Workbooks.Open(str(xls_path.resolve()))
        excel.Calculate()
        wb.Save()
        wb.Close(False)
        if launched:
            excel.Quit()
        print("  Formula cache refreshed via Excel.")
    except Exception as exc:
        print(f"  Warning: Excel recalculation skipped ({exc}) — Python fallback used.")


def _extend_and_recalculate(xls_path, formula_lc=0, new_cols=None):
    """
    Extend formula blocks via Excel COM AutoFill (if new_cols given), then recalculate.
    Called after openpyxl save. More reliable than openpyxl Translator for complex formulas.
    """
    try:
        import win32com.client
        try:
            excel = win32com.client.GetActiveObject("Excel.Application")
            launched = False
        except Exception:
            excel = win32com.client.Dispatch("Excel.Application")
            launched = True

        excel.Visible = False
        excel.DisplayAlerts = False
        wb = excel.Workbooks.Open(str(xls_path.resolve()))

        if new_cols:
            ws = wb.Sheets("US_CPI")
            last_new = max(new_cols)
            for first_row, last_row in FORMULA_BLOCKS:
                src_rng  = ws.Range(ws.Cells(first_row, formula_lc),
                                    ws.Cells(last_row,  formula_lc))
                fill_rng = ws.Range(ws.Cells(first_row, formula_lc),
                                    ws.Cells(last_row,  last_new))
                src_rng.AutoFill(fill_rng, 0)  # xlFillDefault = 0
            print(f"  Formula blocks extended to col {last_new} via Excel COM.")

        excel.Calculate()
        wb.Save()
        wb.Close(False)
        if launched:
            excel.Quit()
        print("  Formula cache refreshed via Excel.")
    except Exception as exc:
        print(f"  Warning: Excel COM operation skipped ({exc}) — Python fallback used.")


