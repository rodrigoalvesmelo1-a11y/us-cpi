def git_push(latest_label):
    cmds = [
        ["git", "add", "cpi_table.html", "CLAUDE.md"],
        ["git", "commit", "-m", f"CPI update: {latest_label}"],
        ["git", "push", "origin", "master"],
    ]
    for cmd in cmds:
        r = subprocess.run(cmd, cwd=str(REPO_DIR))
        if r.returncode != 0:
            print(f"  git command failed: {' '.join(cmd)}")
            break
    else:
        print("Pushed to GitHub.")


